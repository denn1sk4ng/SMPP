<script>
    document.addEventListener('DOMContentLoaded', function () {
        const menuTriggers = document.querySelectorAll('.delete-menu-trigger');
        const floatingMenu = document.getElementById('floatingDeleteMenu');
        const floatingDeleteBtn = document.getElementById('floatingDeleteBtn');

        const modal = document.getElementById('deletePredictionModal');
        const cancelBtn = document.getElementById('cancelPredictionDeleteBtn');
        const confirmBtn = document.getElementById('confirmPredictionDeleteBtn');

        let selectedForm = null;

        menuTriggers.forEach(function (button) {
            button.addEventListener('click', function (e) {
                e.stopPropagation();

                selectedForm = this.closest('.delete-prediction-form');

                const rect = this.getBoundingClientRect();

                floatingMenu.style.display = 'block';
                floatingMenu.style.top = (rect.bottom + 8) + 'px';
                floatingMenu.style.left = (rect.left - 70) + 'px';
            });
        });

        floatingDeleteBtn.addEventListener('click', function (e) {
            e.stopPropagation();

            floatingMenu.style.display = 'none';

            if (selectedForm) {
                modal.style.display = 'flex';
            }
        });

        cancelBtn.addEventListener('click', function () {
            selectedForm = null;
            modal.style.display = 'none';
        });

        confirmBtn.addEventListener('click', function () {
            if (selectedForm) {
                confirmBtn.disabled = true;
                confirmBtn.textContent = 'Deleting...';
                selectedForm.submit();
            }
        });

        modal.addEventListener('click', function (e) {
            if (e.target === modal) {
                selectedForm = null;
                modal.style.display = 'none';
            }
        });

        document.addEventListener('click', function () {
            floatingMenu.style.display = 'none';
        });

        document.addEventListener('keydown', function (e) {
            if (e.key === 'Escape') {
                floatingMenu.style.display = 'none';
                selectedForm = null;
                modal.style.display = 'none';
            }
        });

        window.addEventListener('scroll', function () {
            floatingMenu.style.display = 'none';
        }, true);
    });
</script>
